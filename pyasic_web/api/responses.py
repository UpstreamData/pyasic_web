from typing import List, Literal, Union

from pydantic import BaseModel


class MinerResponse(BaseModel):
    value: Union[float, int, str]
    unit: str = ""


class MinerSelector(BaseModel):
    api_key: str
    miner_selector: Union[List[str], str, Literal["all"]] = "all"
